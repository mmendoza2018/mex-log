		function obtener_datos(id) {
		    var nombre_comp = $("#nombre_comp" + id).val();
		    var serie_comp = $("#serie_comp" + id).val();
		    var  desde_comp= $("#desde_comp" + id).val();
		    var  hasta_comp= $("#hasta_comp" + id).val();
		    var  long_comp= $("#long_comp" + id).val();
		    var  actual_comp= $("#actual_comp" + id).val();
		    var  vencimiento_comp= $("#vencimiento_comp" + id).val();
		    var  estado_comp= $("#estado_comp" + id).val();

		    $("#nombre2").val(nombre_comp);
		    $("#serie2").val(serie_comp);
		    $("#desde2").val(desde_comp);
		    $("#hasta2").val(hasta_comp);
		    $("#long2").val(long_comp);
		    $("#num_actual2").val(actual_comp);
		    $("#fecha_venc2").val(vencimiento_comp);
		    $("#estado2").val(estado_comp);
		    $("#mod_id").val(id);
		}